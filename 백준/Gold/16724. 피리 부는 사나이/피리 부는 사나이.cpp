#include <bits/stdc++.h>

using namespace std;

constexpr char kDeltaY[] =
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\x01\xff\xff\xff\xff\xff\xff\xff\x00\xff\xff\xff"
    "\xff\xff\x00\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff";

constexpr char kDeltaX[] =
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\xff\xff\x00\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff"
    "\xff\xff\x01\xff\xff\x00\xff\xff\xff\xff\xff\xff\xff\xff\xff\xff";

int memo[1 << 20][2];

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int rows, cols;
  cin >> rows >> cols;
  for (int i = 0; i < rows; i++) {
    for (int j = 0; j < cols; j++) {
      char c;
      cin >> c;
      memo[(i << 10) | j][0] = ((i + kDeltaY[c]) << 10) | (j + kDeltaX[c]);
    }
  }

  int ans = 0;
  int cnt = 1;
  for (int i = 0; i < rows; i++) {
    for (int j = 0; j < cols; j++) {
      auto cur = memo[(i << 10) | j];
      if (cur[1]) continue;

      do {
        cur[1] = cnt;
        cur = (int*)memo + (cur[0] << 1);
      } while (cur[1] == 0);

      if ((cur[1] ^ cnt++) == 0) ++ans;
    }
  }

  cout << ans;

  return 0;
}
